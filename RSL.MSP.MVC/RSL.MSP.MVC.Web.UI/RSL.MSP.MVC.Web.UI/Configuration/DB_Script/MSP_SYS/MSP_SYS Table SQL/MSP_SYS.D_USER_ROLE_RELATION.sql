DROP TABLE MSP_SYS.D_USER_ROLE_RELATION CASCADE CONSTRAINTS;

CREATE TABLE MSP_SYS.D_USER_ROLE_RELATION
(
  RELATION_ID    NUMBER                         NOT NULL,
  USER_ID        NUMBER                         NOT NULL,
  ROLE_ID        NUMBER                         NOT NULL,
  IS_VALID       INTEGER,
  CREATE_DATE    DATE                           NOT NULL,
  CREATE_BY      VARCHAR2(20 BYTE)              NOT NULL,
  LASTEDIT_DATE  DATE,
  LASTEDIT_BY    VARCHAR2(20 BYTE)
)
TABLESPACE MSP_SYS_DAT
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          1M
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;

COMMENT ON TABLE MSP_SYS.D_USER_ROLE_RELATION IS '角色用户关系表';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.USER_ID IS '用户ID';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.ROLE_ID IS '角色ID';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.IS_VALID IS '关系是否有效，1有效；0，无效；';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.CREATE_DATE IS '创建日期';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.CREATE_BY IS '修改人';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.LASTEDIT_DATE IS '最后修改时间';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.LASTEDIT_BY IS '最后修改人';

COMMENT ON COLUMN MSP_SYS.D_USER_ROLE_RELATION.RELATION_ID IS '关系ID';


CREATE UNIQUE INDEX MSP_SYS.D_USER_ROLE_RELATION_PK ON MSP_SYS.D_USER_ROLE_RELATION
(RELATION_ID)
LOGGING
TABLESPACE MSP_SYS_DAT
PCTFREE    10
INITRANS   2
MAXTRANS   255
STORAGE    (
            INITIAL          1M
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOPARALLEL;


CREATE OR REPLACE TRIGGER MSP_SYS.TR_D_USER_ROLE_RELATION
   BEFORE INSERT
   ON MSP_SYS.D_USER_ROLE_RELATION    FOR EACH ROW
BEGIN
   SELECT SEQ_D_USER_ROLE_RELATION.NEXTVAL INTO :NEW.RELATION_ID FROM DUAL;
END TR_D_USER_ROLE_RELATION;
